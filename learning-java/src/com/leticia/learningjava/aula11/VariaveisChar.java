package com.leticia.learningjava.aula11;

public class VariaveisChar {

	public static void main(String[] args) {
		
		char o = 'o';
		
		char l = 'l';
		
		char a = 'á';
		
		char feliz = ':';
		
		char alegre = ')';
		
		char o2 = 111;
		
		char i = 105;
		
		char interrogacao = 63;
		
		System.out.println("" + o + l + a + " " + feliz + alegre);
		
		System.out.println("" + o2 + i + " " + feliz + alegre);
		 
		System.out.println("" + "Como vocês estão"+ interrogacao);
		
		

	}

}
