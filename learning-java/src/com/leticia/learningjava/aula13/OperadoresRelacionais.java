package com.leticia.learningjava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int valorUm = 1;
		int valorDois = 2;
		
		System.out.println("valorUm == valorDois: " + (valorUm == valorDois));
		System.out.println("valorUm != valorDois: " + (valorUm != valorDois));
		System.out.println("valorUm > valorDois: " + (valorUm > valorDois));
		System.out.println("valorUm >= valorDois: " + (valorUm >= valorDois));
		System.out.println("valorUm < valorDois: " + (valorUm < valorDois));
		System.out.println("valorUm <= valorDois: " + (valorUm <= valorDois));
	}

}
